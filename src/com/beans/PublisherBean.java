package com.beans;

public class PublisherBean {
	private int userId;
	private String name;
	private long year;
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public long getYear() {
		return year;
	}
	public void setYear(long year) {
		this.year = year;
	}

}
